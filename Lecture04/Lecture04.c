#include <stdio.h>

int main()
{
    // char a[6];
    // - char(1바이트) 원소 6개를 연속(Contiguous)으로 담는 배열.
    // - 인덱스 범위는 0 ~ 5.
    // - 여기서는 "문자 6개"만 저장하고, 문자열 종료문자('\0')는 넣지 않음.
    //   => C에서 "문자열"로 쓰려면 반드시 마지막에 '\0'이 필요하지만,
    //      지금은 문자 하나씩 %c로 출력하므로 '\0'이 없어도 됨(문자열 아님).

    char a[6];

    // 각 원소에 문자 리터럴 저장. 작은따옴표 ' ' 는 "문자 하나"를 의미.
    // a[0] ~ a[5]에 차례대로 L,E,S,_,G,O 저장 → 메모리에는 [L][E][S][_][G][O]
    a[0] = 'L';
    a[1] = 'E';
    a[2] = 'S';
    a[3] = '_';
    a[4] = 'G';
    a[5] = 'O';

    // i는 루프 카운터. 지역 변수라 보통 스택에 잡힘.
    int i = 0;

    // for (i=0; i<6; i++) :
    // - i를 0부터 시작해서 5까지 증가시키며 총 6번 반복.
    // - 배열 범위를 정확히 맞춤(넘치면 미정의 동작/메모리 오염).
    for (i = 0; i < 6; i++)
    {
        // printf("%c", a[i]);
        // - %c : 단일 문자 출력용 서식지정자.
        // - a[i]는 i번째 문자를 반환.
        // - 여기서는 문자열이 아니라 "문자 하나씩" 찍기 때문에 '\0' 불필요.
        // - 주의: 만약 "%s"로 찍었다면 '\0' 없어서 미정의 동작(쓰레기 출력/크래시)!
        printf("%c", a[i]);
    }

    // 개행이 없어서 프롬프트와 출력이 붙어 보일 수 있음. 보기도 좋게 하려면:
    // printf("\n");

    return 0; // 정상 종료
}